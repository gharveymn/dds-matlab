function registerdxt
	formatStruct(1) = struct('ext',         {'dds'},...
						'isa',         @isdds,...
						'info',        @imddsinfo,...
						'read',        @readdds,...
						'write',       @writedds,...
						'alpha',       1,...
						'description', 'DirectDraw Surface');
					
	formatStruct(2) = struct('ext',         {'hdr'},...
						'isa',         @ishdr,...
						'info',        @imhdrinfo,...
						'read',        @readhdr,...
						'write',       @writehdr,...
						'alpha',       0,...
						'description', 'RGBE (Radiance HDR)');
					
	formatStruct(3) = struct('ext',         {'tga'},...
						'isa',         @istga,...
						'info',        @imtgainfo,...
						'read',        @readtga,...
						'write',       @writetga,...
						'alpha',       1,...
						'description', 'Truevision TGA (TARGA)');
					
	imformats('add', formatStruct);
					
end