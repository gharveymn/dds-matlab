function metadata = ddsfinfo(filename, varargin)
	%DDSREAD Summary of this function goes here
	%   Detailed explanation goes here
	
	validateattributes(filename,{'char'},{'nonempty'},'ddsfinfo','FILENAME',1);
	
	if(nargin > 1)
		validatestring(varargin, {'None',               ...
						      'LegacyDWord',        ...
						      'NoLegacyExpansion',  ...
						      'NoR10B10G10A2FixUp', ...
						      'ForceRGB',           ...
						      'No16BPP',            ...
						      'ExpandLuminance',    ...
						      'BadDXTNTails',       ...
						      'ForceDX10Ext',       ...
						      'ForceDX10ExtMisc2'}, 'ddsfinfo', 'FLAGS');
	end
	metadata = ddsio('READ_METADATA',filename, varargin);
	
end
